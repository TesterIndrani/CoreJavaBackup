package wrapperClasses;
//implicitly converted from non-primitive to primitive without value() method

public class AutoUnBoxing {

	public static void main(String[] args) {
	
		Integer i=10;	//non-primitive value
		int a=i;
		System.out.println(a);	//primitive
		
		Double j=78.90;		//non-primitive value
		double d=j;
		System.out.println(d);	//primitive
		
	}
}
